	<h2 id="Gameplay">Gameplay</h2>
	<p>I loathe competitive “E-Sports”-like games. I play CS:GO for fun and get shouted at in Russian for being bad at it. I hate Football. No really, I 
	am absolutely, completely opposed to Football or Soccer as you Americans call it. Except for a small fling during the European Championship that Austria 
	hosted I do not care for it. I mostly hate playing with Console Gamers since I usually get unpleasant facts about my Mother or my sexual orientation.</p>
	<p>So color me surprised when I found myself playing a game that combines Football (or Soccer, get used to it), cross-platform play and E-Sports with the 
	addition of cars. Not only playing it… but actually really liking it. I am, of course, talking about Rocket League. A game that is best described as 
	“Soccer with rocket-powered RC Cars”.</p>
	<p>Released in 2015 it is a remake (albeit that the Developers call it sequel) of 2008's Supersonic Acrobatic Rocket-Powered Battle-Cars with better graphics 
	and a name that does not break your brain. Both were developed by Psyonix, a California based Studio that mostly programmed for other Studios. Both games are 
	very close in spirit but the new iteration did some things differently. Such as being released for Consoles and PC and being free with a Playstation Network 
	Gold subscription. Also PS4 and PC gamers can play against (or with) each other, a feature that will not be included in the upcoming Xbox One Version since 
	Microsoft opposes cross-platform play.</p>
	<h2 id="Story">Story</h2>
	<p>Let’s pretend that this here is a Story for my Editors sake okay?</p>
	<p>Some things are the best when they are simple, but intricate when you get the hang of it. This is Rocket League in a nutshell. You are part of one 
	of two teams, each consisting of 1 to 4 cars and you have five minutes to shoot more goals against the opposing team than they shoot against you. For 
	that goal your car can jump and use boost that gets refilled by glowing platforms around the arena. There, you now know all there is to this game. 
	Two Sentences. What you don’t know is to use your boost to soar through the arena, use the walls to score goals, trick the opposing team into going 
	the wrong direction so you can score and much, much more. And the truth is… you need time to learn this but you do so. Gradually, each play will be 
	better than your last and before you know it you are scoring aerial goals and wallriding to glory. And the whole process from starting out to honing 
	your skills does go by in an amazingly fun way. Never do you feel you are the wounded animal keeping the Team back. Mostly thanks to the great 
	(with flaws, I get to that later) Matchmaking and the seamless learning by doing.</p>
	<p>The game features a Tutorial that shows you the basics and teaches you the more intricate dynamics of Car-Soccer.  A Single player mode in which 
	you compete with the A.I. that can be changed from beginners to absolute Pros, in either a complete Season or a Single Match. And multiple online Modes 
	in ranked (Your progress is tracked and you gain or lose points depending on the performance of your Team) or the “just for fun” Unranked modes, 
	each ranging from 1 vs. 1 to 4 vs. 4.</p>
	<p>Of course you can set up a Team with your Friends and compete against what the World of Matchmaking throws at you. A feature I highly recommend 
	if you are going for a Ranked play as… well, people that you get on your Team might not always be skilled or interested for your Team to win. </p>
	<p>Which brings me to one of the flaws of the game. Matchmaking.  Don’t get me wrong in around 80% of times the Teams are well balanced and fair 
	but sometimes the game fails to find suitable players for a few minutes, making you wait for the game to start and you get assigned an A.I. Player that 
	usually bears little to no help for your Team. Even more they actually can be a horrible burden on your play, shooting the ball back directly at your goal. 
	A mistake that the human players have to cope with enough without an A.I. that seems to be outright sabotaging you.</p>
	<p>The next flaw is that you play against humans and some of them will just get up and leave your game when your Team seems to be a tad behind 
	or they make an embarrassing mistake. Getting you a new A.I. Player and a further increase in difficulty to make up for whatever made them leave.  
	Which in turn means quite a lot of times you get thrown into matches where the team you join is six goals behind and with one minute to go you might 
	not feel entirely motivated to give it your best.</p>
	<p>Those flaws do little to the overall fun of the Game and its simplistic but grand appeal to even the most hardcore hater of the elements that make up the game.</p>
	<p>The sometimes wonky network coding and physics is much more damaging in that regard. Depending on your ping and quality of service connection issues can be 
	frequent and not always does a collision with the ball or an enemy result in what you’d expect, even in the realms of the game. This is something you will 
	have to get used to as, in my personal experience, the patches for the game seem to not touch the issues very much.</p>
	<p>Still, I play it and I enjoy it but I could see that with sub-optimal internet connections this could be a major issue that might make the game borderline unplayable for some.</p>
	<h2 id="Graphics">Graphics, Settings and Performance</h2>
	<h4>Graphics</h4>
	<p>The game hosts an avalanche in configuration options and should run in flawless 60 FPS on any modern Computer. You can set everything from FOV to camera 
	shakes with sliders and checkboxes. It is not graphically stunning but no eyesore either. That it seems to be made primarily for Consoles is virtually unnoticeable 
	except for the Controls. A Gamepad is highly recommended since Mouse and Keyboard work okay but the Gamepad seems to have that little edge over it especially with 
	aerial maneuvering.</p>
	<h2 id="Conclusion">Conclusion</h2>
	<p>As unlikely as I would ever put it. I love this game as much as it makes me rage sometimes.  I would give it a perfect score if it ever addressed the 
	issues of wonky network connections and physics and the sometimes pretty bad matchmaking. But overall it is a thoroughly enjoyable game that makes me feel 
	like I am Ronaldo shooting exploding balls at goals… A dream I should tell my psychiatrist at some point.</p>
