	<h2>Binding Of Isaac</h2>
	<p>In short, The Binding of Isaac: Rebirth is the best game I have ever played, and the long answer … well that’s the rest of the review.</p>

	<h2 id="Gameplay">Gameplay</h2>
	<p>Now don’t get me wrong, this isn’t a game I binge on and spend 8 hours at a time playing but I have managed to consume nearly 500 hours of quality 
	content from this £11 purchase, this has become the game that I have popped in and out of for an hour here and there almost every day for the last year.</p>
	<p>The game is controlled in the classic twin stick shooter style, WASD moves, while the arrow keys shoot. These controls are incredibly tight and allow 
	the user the perfect amount of control for the precision bullet hell dodging that is required throughout the game. Even if you find the keyboard controls 
	a little bit off you can simply plug in a gamepad and enjoy it that way, I have played both ways and find both very usable with neither left me with a distinct 
	disadvantage.</p>
	<p>The range of bosses that round off each level is incredible, there’s a cleft lipped blob of muscle that bounces towards you at astounding speeds, a pile of 
	sh*t and your own mother’s foot, eyes and ominous blobs of fat. All of which are terrifying to a novice player and manage to still challenge an experienced player while 
	still being fair. </p>
	<p>One of trends of gaming in the last 10 years that annoys me to no end is a lazy developer (s) who puts out a game and leaves it there despite the fact that it 
	is broken, unbalanced and buggy. This couldn’t be further from the truth with Edmund MacMillan and Tyrone Rodriguez; they are constantly tweaking the way the game 
	works, whether or not the core audience likes their changes. The ARG revolving around the unveiling of the new character was one of the most in depth and exciting 
	moments that I have followed in gaming in the last few years and I have an incredible amount of respect and adoration for MacMillan and Rodriguez for putting their 
	time and effort into making this game.</p>
	<p>Before this review becomes too much of a love letter I would like to point out that this game is by no means the perfect game, for example the fact that you have 
	to fight the exact same boss on 2 floors in every run that you do can get somewhat repetitive. The biggest issue I have is the way that the devs have handled difficulty, 
	pre-afterbirth DLC the secret hidden character called The Lost was the most challenging and often infuriating challenges within the game. The character had no life and 
	would die after one hit. When the DLC dropped they added a lot of power ups to the character which made it much easier to complete, this was because they wanted the new 
	“Keeper” character to be the most challenging and it has left concerns in my mind that when the next DLC drops then the keeper will be demoted to make the new character 
	more difficult and thus making the effort that I gave to finish the Keeper null and void.</p>
	<h2 id="Story">Story</h2>
	<p>The story is where this game gets … interesting. Not in the “oh wow, the details of modern life and western society featured in this game are simply fascinating and 
	have truly opened up my eyes to my own personal downfalls”, it’s more of a “huh … interesting”. The game opens with a cut scene that explains that you play Isaac and your 
	mother has attempted to murder you because of her religious beliefs and that you are on the run. A little bit strange to begin with but it is once you have completed the game 
	on multiple occasions that the game becomes truly strange. Once you have had an encounter with mum and killed her, you climb up inside her womb, from there you somehow find her 
	heart and kill that too, from there you have the decision to go to a cathedral or the Sheol. From there you must choose whether to go to the chest to fight a dead baby or to 
	the dark room to fight the lamb.</p>
	<p>As you can tell the story is not really where this game shines, it may read like a game that is trying too hard to be different and edgy but you can easily play the game 
	paying no attention to the story whatsoever and still enjoy it and in my opinion this is the best way to play this game. In my opinion this game is the defining proof that 
	if game play is done correctly then the story can be lacklustre.</p>
	<h2 id="Graphics">Graphics, Settings and Performance</h2>
	<h4>Graphics</h4>
	<p>This isn’t a game that you can stand still in and just look around, in the way that Witcher 3 is. Although it isn’t drop dead gorgeous, the way that the game presents 
	itself is very aesthetically pleasing and the pixel art in the game is quite impressive. I’m not a fan of the recent trend of every other game being “retro-style” but 
	if I had to choose between this and the previous games look of cheaply drawn flash animation then I would easily choose the pixel art. </p>
	<h2 id="Conclusion">Conclusion</h2>
	<p>There isn’t much more I can say about the Binding of Isaac, it’s the closest to perfect any game I’ve played has managed to achieve. There are so many times 
	during this writing this that I have gone back to the game and played it for a couple of hours (hence why it has taken me 2 weeks to finish this). This is the first 
	game that I have played that captured my heart in this way since I was 9 years old playing The Simpsons Hit and Run. My ability to continue to play this game daily 
	after a year of non-stop play has confused and astounded everyone on my steam friends list.</p>
	<p>I can’t blush more about this game anymore and I can’t recommend it enough, so it’s because of that I am going to give this game a 9/10. Not quite perfect but oh so close.</p>
