	<h2>Relic Hunters Zero</h2>
	<p>Relic Hunters Zero is a free, open-source top-down shooter about fighting evil ducks and turtles, and supports controllers and local multiplayer with up to 2 players if one or both use a controller. What a concept.</p>

	<h2 id="Gameplay">Gameplay</h2>
	<p>Relic Hunters plays a lot like Magicka 2, except you have guns. There are 3 types of weapons in Relic Hunters: Light, Medium and Heavy. Weapons are classified 
	by which ammo they use, not their size. Light weapons and ammo is very easy to find, and doesn’t do much damage. The light guns often focus of fire rate over 
	accuracy, and are obsolete by stage 3. Medium weapons can only be picked up stage 2 or later, and are a good balance of power and rarity. Medium ammo is also 
	relatively common, and most medium weapons don’t burn through it, as they focus on firepower over range or speed. Medium weapons can be useful up to stage 4, with 
	the exception of the Rocket Launcher, which has to be bought from a shop, and uses more ammo per shot. Heavy weapons are the most powerful weapons in the game, but 
	also the rarest. They are also very varied, with the Sniper Rifle doing massive damage but using 20 ammo per shot, to the Assault Rifle, a burst weapon.</p>
	<p>Each of the six characters in Relic Hunters Zero has unique abilities, strengths and weaknesses. You start with 2 characters, JImmy, an all-rounder and 
	Pinkyy (not a typo), who is stronger than Jimmy in every way, but starts with only a melee weapon. Each character has a special weapon/item that changes the 
	game. Jimmy has the Elite Pistol, which is the strongest light weapon in the game. Pinkyy has the Melee Fists, which do more damage that the normal melee. 
	Raff has less health than Jimmy but can shoot through walls with his Keytar. Biu has more health than anyone, but his shield can only absorb 20 damage, compared 
	to the usual 60, and he does less damage but gets double grenades. Punny spawns with a flamethrower and a stronger shield, but has less HP and has a damage 
	penalty on Light and Medium weapons. Finally, Ace has the worst stats in the game and spawns with the worst gun, but gets a massive money boost and is luckier 
	than others.</p>
	<p>The shop system is also one of the most unique ideas I’ve ever seen. Anything can be brought from the shop, from ammo, to overshield to weapons. Everything 
	is also very expensive, but once brought, it will be permanently available at spawn, meaning buying requires planning. Bought a Heavy Machinegun? Better buy some 
	Heavy Ammo so you can spawn with some bullets.</p>
	<p>The boss fight, however, is pretty lackluster. You fight the Duke Ducan, a baby T-Rex, and the battle consists of: destroying a shield generator, hitting 
	things at the boss while shooting him, killing some high level AI, repeat. With the rest of the game being so great, I was expecting more from the battle, but the 
	game’s free, so I can’t really be that angry.</p>
	<p>Once you beat the game you unlock endless mode, which has less ammo scattered around, and enemies don’t drop guns. However, when you clear a stage you go 
	to a shop that uses special ‘Endless credits’ that you lost once you die. You can buy and upgrade weapons at this shop. Endless mode is fun if you bring a friend, 
	but on its own it gets stale quickly.</p>
	<p>All this hasn’t touched on the main purpose of the game though. Hunting Relics. Each of the Relics has 3 pieces that can be dug up once a sector is completed, 
	or if you can’t find it, can be bought from the shop later. Once a relic is completed, you get a game changing power, like ammo regen, permanent shield boost, or more 
	health. You can have up to three relics active at once, some complement others. For example, you could have a shield boost relic and a shield regen relic working together 
	to make you harder to permanently damage. These combinations are critical when building your character, as they can compensate for a weakness like low health or enhance a 
	strength to frankly stupid levels.</p>
	<h2 id="Story">Story</h2>
	<p>Relic Hunters Zero has a very basic story. The evil Duke Ducan is attempting to gather the ancient relics from the Asteroid Dungeon Nemesis and rise with his duck 
	army to rule the galaxy. You arrived on the Spaceheart with the other hunters and need to stop the Ducan army. The story seems to be purposely melodramatic, as a joke, 
	and it really never shows up until you beat Duke Ducan.</p>
	<h2 id="Graphics">Graphics, Settings and Performance</h2>
	<h4>Graphics</h4>
	<p>Relic Hunters doesn’t have the best looking graphics, for sure, but the hand drawn illustrations on menus and semi-pixelated look of the actual game are almost 
	ageless and the bright colours and flashing lights fit in with the cartoonish, over-the-top look of the game.</p>
	<p>The settings menu is pretty bare, and features the usual volume and keymapping options, as well some basic options for particles and objects so you can run 
	the game on older PC’s. However, the game is trivial to run, using only 7% of my desktop rig’s power. I even managed to run the game on an ancient XP box, using 
	integrated graphics. However, it would drop to about 45 FPS, even with all settings turned down.</p>
	<p>The menu also featured the option to reset the game, and a button called ‘Gameplay Options’. Curious, I clicked it. And behold, some of the most useful 
	settings I’ve ever seen, like double-tap dash, always show ammo bars (normally it only shows when you pick up ammo or reload), screen shake, 3 aim assist modes 
	for controller (Off, Assist and Full) and many more.</p>
	<h2 id="Conclusion">Conclusion</h2>
	<p>Relic Hunters Zero is a fast, fun arcade-style top-down shooter that everyone should try. I would happily pay $10, maybe more, for this, but it’s free, 
	and not a single DLC or IAP to boot. The controls are responsive, there’s a good choice of weapons, characters and upgrades and it’s open source. I would recommend 
	you get a friend or family member, hook up your rig (or laptop, it’s not hard to run) to the TV, grab a controller or two, and have one of the most casual, fun gaming 
	experiences I’ve had in a long time. And for a total price of free, there’s no reason to. I give it a very solid 9/10.</p>
