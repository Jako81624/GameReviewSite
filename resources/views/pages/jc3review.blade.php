	<h2>Just Cause 3...</h2>
	<p>... is the successor to, (no surprises here), its very well received predecessor, "Just Cause 2".  The
	question is though, does it fill the canoe-sized shoes of its older brother?</p>

	<p>The short answer is yes, but only in some places.</p>

	<h2 id="Gameplay">Gameplay</h2>
	<p>Just Cause 3 follows the "Michael Bay" mentality - the more explosions on and around the screen the
	better.  The gameplay largely consists of simply constructing large and bombastic explosion sequences.
	The way you do it?  Up to you.</p>
	<h4>Weapons</h4>
	<p>Just Cause 3 is not a tactical shooter.  Whilst not an excuse, this does explain why most guns feel
	very "samey" and lack variety.  Whether its dual wielding SMGs, or using the overly large and powerful
	LMGs, the only difference really lies in rate of fire, damage and slight spread differences.  This isn't
	CS:GO, so don't go in expecting the guns to act like it.</p>
	<p>Explosives are a big part of JC3, and to make sure you always have access to them it gives you unlimited
	GE-64 (the games equivalent of C4) to mess around with.  A fantastic design choice in my opinion.  When
	you start, only three can be placed at once, but by doing challenges you can unlock "Mods" that allow you
	to place up to five and even fit them with booster rockets.  The mayhem you can create with these is
	borderline endless.
	<p>Whilst on the topic of explosives, there are many types of Grenades, RPGs and vehice-mounted rockets
	in the game.  Homing rockets, grenade launchers and grenades that explode upon impact are all fair game
	here.  What you do with them and how?  Your perogative.</p>
	<p>Vehicular Weaponry really hasn't differed much from JC2.  You get your HMGs, Rockets and Miniguns
	attached to different vehicles, but with the hugely powerful arsenal Rico carries around with him,
	you probably won't find much use for them.</p>
	<h4>Vehicles</h4>
	<p>Just Cause 3 isn't a driving sim and thats very obvious.  Cars and bikes go fast and can be fitted with nitrous
	and the ability to nitrous jump - an ability that is exactly what it says on the tin.  The slightest
	touch to another vehicle sends it careering off the road and every vehicle becomes a makeshift
	explosive if you get out whilst its still moving.  Fun, but not an integral part of the experience.
	You can take these vehicles to garages in liberated areas of the map and have them unlocked for deployment
	anywhere if you so desire, but thats totally up to you.</p>
	<p>Flying Vehicles such as Jets and Helicopters are a lot of fun.  That's all I really have to say.  You
	take them, have a joyride, and let it go.  Whilst JC2 made Rico's arsenal pale in comparison with helicopters
	and Jets, this is no longer the case.  Rockets and homing missiles are great don't get me wrong, but
	Rico now carries the very weapons that replaced his arsenal in the previous game on his back.  Honestly,
	I'm slightly exaggerating.  But not by much.  All I'm saying is they feel slightly nerfed in comparison
	to 2, but maybe that's a good thing.  Sound off in the comments below.</p>
	<p>Boats also exist as part of the game.  That's it.  They exist.  Not really a huge part of the game
	honestly.  You can hijack big military ships with cannons though, so its got that going for it.  Which
	is nice.</p>
	<h4>Getting around the world</h4>
	<p>Wingsuiting.  Whoever thought it was a good idea in this game deserves a medal.  I find myself
	using it more in place of fast travel than I ever thought I would and its very rewarding to pull
	off stunts and glide through tunnels, overtaking cars as you go.  Whilst it feels unresponsive in
	some places, usually the wingsuits feels fun and fantastic.  One of the best parts of the game in
	my opinion, and whilst not a new addition to the videogame industry (see Fary Cry 4), I feel
	like this is the best implementation of it so far.  Bravo.</p>
	<p>The parachute is still here, but feels different.  You can still parachute climb with it, 
	but it feels a little more weighty compared to before.  Probably due to the existence of the
	wingsuit.  However, it is a much more stable shooting platform and is still pretty quick.
	The feeling of raining down grenades and fire onto gangs of mercenaries is still there, and its
	even better this time around.</p>
	<h4>Challenges and Map Contents</h4>
	<p>The map size is the same as JC2, 1000km^2 to be precise.  That's a lot of space to fill.  JC2
	suffered from an issue of lack of density, where a lot of the map was simply empty jungle and
	lacked anything to do.  JC3 doesn't have this problem, the world feels buzzing with activity and
	there's always something to do whether it's a set mission or random occurence.  Huge props to
	Avalanche on that front.</p>
	<p>However, its not all sunshine and roses.</p>
	<p>Just Cause is a series about destruction, and JC3 is no different as I've said before.  This means
	that the content introduced is very "samey" since you will be doing the same job over and over again.
	In this case, its called "Liberating".  To unlock challenges and even story missions, you have to
	Liberate Settlements from the games villain "Di Ravello".  This basically involves destroying
	stuff and causing chaos in said settlements.  Some will have more things to activate and destroy,
	some not so much.  Either way, the process is the same.  Take the same process and rinse and repeat
	it for hours and you have the liberation system.  In my opinion, no amount of explosions takes away
	from the fact that things get boring after a while.  The case of JC3 is no different.  The pretty
	explosions are great, but they don't take away from the eventual boredom of doing the same thing
	over and over again.  I kind of wish that every settlement had something slightly unique, but at the
	same time I understand the amount of time a developer would have to put into a feature like that.
	A little bit sad really, but at the same time predictable.  The same prospect applies to military bases,
	which are effectively settlements with military installations to destroy and more heavy guarding.  Its
	worth noting that to liberate some military bases you will have to complete a mission disabling its
	heavy defenses, what the game calls "FOWs".  These can be nukes, emps or any manner of powerful
	AOE weaponry that affects you, vehicles or both.  A nice feature, but an infuriating one at that.</p>
	<h2 id="Story">Story</h2>
	<p>Lets be honest here, you're not here for the story are you.  So lets move on from that.  The story
	isn't stellar or heart-wrenching.  No spoilers, but the story basically goes:</p>
	<ol>
	<li>Go to home country</li>
	<li>Blow up bits of home country</li>
	<li>Kill dictator</li>
	<li>Put old dictator back in power</li>
	</ol>
	<p>Jokes aside, the story doesn't feel great.  But it doesn't have too.  Just Cause 3 is fun in its own
	right, the story is there more for necessity and unlocking stuff more than anything.  There are some
	twists and turns along the way, but its not the most detailed and mind-bending story out there.</p>
	<h2 id="Graphics">Graphics, Settings and Performance</h2>
	<h4>Graphics</h4>
	<p>Graphically, JC3 is stellar in almost every aspect.  The world is colorful and vibrant, the explosions
	surprisingly realistic and the scenery almost lifelike.  Character detail is a huge step up from JC2.
	Saying all this, however, the game isn't a Crysis.  It's not the most hyper realistic game out there
	graphically, but there's no denying it looks absolutely stunning especially with everything cranked up.</p>
	<p>That is, if you can.</p>
	<h4>Performance</h4>
	<p>Performance, as it stands, has been and still is a mixed bag for many users with bugs such as memory leaks
	and frame dips being reported by hundreds of players.  In my experience, my 12gb of RAM, FX-8350 and GTX 970
	(all latest drivers) maintained a good 60fps and well above in most places, but dropped severely when
	there was a lot on the screen, particularly in explosive sequences.  Counteracting this, however, is the
	fact that all destructible items exist as physics objects in the world when blown up.  So that partially
	explains the FPS drop.  However, no amount of settings change will change the fact about physics.  People
	with intel processors are generally reporting better frame rates though, potentially due to their improved
	SMT support vs AMDs lack of.  What isn't justifiable, however, is the hitching and lag caused by the online
	leaderboards.  These leaderboards will activate whenever you do something record worthy like longest time
	wingsuiting or most explosions in x seconds.  For some reason, this feature has been reported as to cause
	frame drops every few seconds in some cases and I can confirm this.  Putting steam in offline mode or
	blocking JC3 from accessing the internet does fix this particular problem.  Another issue I have with it is the
	mandatory online activation the first time you use it.  I understand why, but now its asking me to
	activate "for the first time" on my laptop again.  Not great when you're in college and most game services
	are blocked.  Not that I game in college.  Why would I do that?</p>
	<h4>Settings</h4>
	<p>This being Avalanche, the settings for everything from key rebindings to graphical options are all
	present and plentiful.  All keyboard keys can be rebound, you have the standard batch of audio settings
	and there are of course the graphical settings.  The graphical settings are good to very good, with the
	usual Texture level changes along with Bokeh DOF and other more exotic settings.  However, people like
	Totalbiscuit will not appreciate the lack of FOV adjustment.  Whilst I'm sure its adjustable in ini files,
	this is 2015 and not having an FOV slider in a triple A title isn't really justifiable anymore.</p>
	<h2 id="Conclusion">Conclusion</h2>
	<p>Just Cause 3 suffers from the usual open world disease: samey content and little variation.  But for what
	it does have and what it does do, it does very well.  The game is beatiful, explosions and incredible and
	the wingsuit is amazing.  The story is a little lacking, the driving arcadey and the map huge and bustling
	with activity.  If all the good points in that little spiel outweight the negative, then I suggest you
	buy it now.  Just make sure you meet the requirements set on the steam page.  Also, if you have frame drops
	etc, put steam in offline mode and relaunch the game.  Seems to be a common fix for a lot of people.</p>
	
